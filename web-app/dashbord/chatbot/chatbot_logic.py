    def analyze_sentiment(self, text):
        """تحليل مشاعر النص"""
        try:
            blob = TextBlob(text)
            polarity = blob.sentiment.polarity
            if polarity > 0.3:
                return "positive"
            elif polarity < -0.3:
                return "negative"
            return "neutral"
        except:
            return "neutral"

    def get_user_data(self, email):
        """جلب بيانات المستخدم من Firebase"""
        if not self.db or not email:
            return None
        try:
            users_ref = self.db.collection('MyUser')
            query = users_ref.where('email', '==', email).limit(1)
            docs = query.get()
            for doc in docs:
                return doc.to_dict()
            return None
        except Exception as e:
            print(f"Error getting user: {e}")
            return None

    def get_user_balance(self, email):
        """جلب رصيد المستخدم"""
        if not email:
            return None
        user = self.get_user_data(email)
        if user:
            accounts = user.get('accounts', [])
            total_balance = sum(acc.get('balance', 0) for acc in accounts)
            return total_balance
        return None

    def get_user_loans_status(self, email):
        """جلب حالة قروض المستخدم"""
        if not email:
            return None
        user = self.get_user_data(email)
        if user:
            loans = user.get('loans', [])
            if loans:
                active_loans = [l for l in loans if l.get('status') in ['active', 'approved']]
                pending_loans = [l for l in loans if l.get('status') == 'pending']
                rejected_loans = [l for l in loans if l.get('status') == 'rejected']
                return {
                    'total': len(loans),
                    'active': len(active_loans),
                    'pending': len(pending_loans),
                    'rejected': len(rejected_loans)
                }
        return None

